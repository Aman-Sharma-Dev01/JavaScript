let month = 9

switch (month){
    case 1:
        console.log("jan");
    break

    case 2:
        console.log("feb");
    break
    case 3:
        console.log("march");
    break
    case 4:
        console.log("april");
    break
    case 5:
        console.log("may");
    break
    case 6:
        console.log("june");
    break
    case 7:
        console.log("july");
    break
    case 8:
        console.log("august");
    break
    case 9:
        console.log("sep");
    break
    case 10:
        console.log("oct");
    break
    case 11:
        console.log("nov");
    break
    case 12:
        console.log("dec");
    break
    default:
        console.log("invalid input");
    break
}